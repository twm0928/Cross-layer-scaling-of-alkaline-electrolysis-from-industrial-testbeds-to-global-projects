function publish_final_dynamic_dataset_r1()
%PUBLISH_FINAL_DYNAMIC_DATASET_R1 Lock the current R1 970-scenario dynamic dataset.
%
% This script archives the current accepted dynamic-surrogate evidence
% chain into outputs/final_locked so that the manuscript figures, feature
% analysis, and plant-level dynamic evaluation all point to one frozen
% version of the 970-scenario dataset.

src_dir = fileparts(mfilename('fullpath'));
flow_root = fileparts(src_dir);
module_root = fileparts(flow_root);
workflow_root = fileparts(module_root);
project_root = fileparts(workflow_root);

output_dir = fullfile(flow_root, 'outputs');
final_dir = fullfile(output_dir, 'final_locked');
figure_data_dir = fullfile(project_root, 'Figure', 'Figure 3a', 'data');

if ~exist(final_dir, 'dir'); mkdir(final_dir); end

addpath(src_dir);
scenario_ids = get_r1_dynamic_scenario_ids('combined970');
scenario_table = table((1:numel(scenario_ids))', scenario_ids(:), ...
    'VariableNames', {'combined970_order', 'scenario_id'});

scenario_csv = fullfile(final_dir, 'scenario_ids_r1_combined970_final_locked.csv');
scenario_xlsx = fullfile(final_dir, 'scenario_ids_r1_combined970_final_locked.xlsx');
scenario_mat = fullfile(final_dir, 'scenario_ids_r1_combined970_final_locked.mat');
writetable(scenario_table, scenario_csv);
writetable(scenario_table, scenario_xlsx);
save(scenario_mat, 'scenario_ids', 'scenario_table');

copy_with_check(fullfile(output_dir, 'eta_model_r1_970_training_data.csv'), ...
    fullfile(final_dir, 'eta_model_r1_970_training_data_final_locked.csv'));
copy_with_check(fullfile(output_dir, 'eta_model_r1_970_metrics.csv'), ...
    fullfile(final_dir, 'eta_model_r1_970_metrics_final_locked.csv'));
copy_with_check(fullfile(output_dir, 'eta_model_r1_970_benchmark.csv'), ...
    fullfile(final_dir, 'eta_model_r1_970_benchmark_final_locked.csv'));
copy_with_check(fullfile(output_dir, 'feature_subset_candidates_r1.csv'), ...
    fullfile(final_dir, 'feature_subset_candidates_r1_final_locked.csv'));

copy_with_check(fullfile(figure_data_dir, 'Fig3a_R1_dynamic_efficiency_long.csv'), ...
    fullfile(final_dir, 'Fig3a_R1_dynamic_efficiency_long_final_locked.csv'));
copy_with_check(fullfile(figure_data_dir, 'Fig3a_R1_dynamic_efficiency_binned.csv'), ...
    fullfile(final_dir, 'Fig3a_R1_dynamic_efficiency_binned_final_locked.csv'));
copy_with_check(fullfile(figure_data_dir, 'Fig3a_R1_dynamic_efficiency_summary.csv'), ...
    fullfile(final_dir, 'Fig3a_R1_dynamic_efficiency_summary_final_locked.csv'));
copy_with_check(fullfile(figure_data_dir, 'Fig3a_R1_dynamic_efficiency_M1_M7.xlsx'), ...
    fullfile(final_dir, 'Fig3a_R1_dynamic_efficiency_M1_M7_final_locked.xlsx'));

manifest_file = fullfile(final_dir, 'manifest_dynamic_final_locked.txt');
manifest_lines = { ...
    'R1 dynamic surrogate final-locked package'; ...
    ['Generated: ' char(datetime('now', 'Format', 'yyyy-MM-dd HH:mm:ss'))]; ...
    'Scenario scope: combined970 = 730 natural + 240 generated'; ...
    ['Scenario count: ' num2str(numel(scenario_ids))]; ...
    ''; ...
    'Locked files:'; ...
    '  scenario_ids_r1_combined970_final_locked.csv/.xlsx/.mat'; ...
    '  eta_model_r1_970_training_data_final_locked.csv'; ...
    '  eta_model_r1_970_metrics_final_locked.csv'; ...
    '  eta_model_r1_970_benchmark_final_locked.csv'; ...
    '  feature_subset_candidates_r1_final_locked.csv'; ...
    '  Fig3a_R1_dynamic_efficiency_long_final_locked.csv'; ...
    '  Fig3a_R1_dynamic_efficiency_binned_final_locked.csv'; ...
    '  Fig3a_R1_dynamic_efficiency_summary_final_locked.csv'; ...
    '  Fig3a_R1_dynamic_efficiency_M1_M7_final_locked.xlsx'; ...
    ''; ...
    'Purpose:'; ...
    '  1) freeze the accepted 970-scenario dynamic dataset'; ...
    '  2) freeze the accepted Fig. 3a dynamic envelope source'; ...
    '  3) provide a stable input package for later plant-layer evaluation'; ...
    };
write_text_lines(manifest_file, manifest_lines);

fprintf('Dynamic final-locked package created at:\n  %s\n', final_dir);
end

function copy_with_check(src, dst)
if ~isfile(src)
    error('Missing source file: %s', src);
end
[ok, msg] = copyfile(src, dst, 'f');
if ~ok
    error('Failed to copy %s -> %s\n%s', src, dst, msg);
end
end

function write_text_lines(filename, lines)
fid = fopen(filename, 'w');
if fid < 0
    error('Failed to open text file for writing: %s', filename);
end
cleanup = onCleanup(@() fclose(fid));
for i = 1:numel(lines)
    fprintf(fid, '%s\n', lines{i});
end
end
